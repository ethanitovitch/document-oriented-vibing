export function createFeatureTemplate(featureFileName: string): string {
	const title = featureFileName.replace(/\.md$/i, '').replace(/-/g, ' ');
	return [
		`# Feature: ${title}`,
		'',
		'## Diagram',
		'',
		'```dov',
		'flow LR',
		'Start(Start) > Step[Step 1|src/example.ts]',
		'Step > End(End)',
		'```',
		'',
		'## Summary',
		'Describe intent, constraints, and expected behavior.',
		'',
	].join('\n');
}
