export const DOV_SCHEMA_VERSION = 19;
export const LLM_INSTRUCTIONS_START = `<!-- dov-start v${DOV_SCHEMA_VERSION} -->`;
export const LLM_INSTRUCTIONS_END = `<!-- dov-end -->`;
export const LLM_INSTRUCTIONS_MARKER = LLM_INSTRUCTIONS_START;
export const DOV_SKILL_VERSION_MARKER = `<!-- dov-skill v${DOV_SCHEMA_VERSION} -->`;
const DEFAULT_EXTENSION_ID = '<installed-extension-id>';

export interface DovInstructionOptions {
	extensionId?: string;
}

// Matches any version of the dov block (v1, v2, …)
export const DOV_BLOCK_REGEX = /<!-- dov-start v\d+ -->[\s\S]*?<!-- dov-end -->/g;
// Also match the old v1 marker that had no end tag
export const DOV_LEGACY_REGEX = /<!-- document-oriented-vibing(?:-v\d+)?[\s-]*-->[\s\S]*$/;

function getCaptureReviewCommand(reviewName: string, options: DovInstructionOptions = {}): string {
	const extensionId = options.extensionId?.trim() || DEFAULT_EXTENSION_ID;
	return `code --open-url "vscode://${extensionId}/captureReview?name=${reviewName}&threadId=$CODEX_THREAD_ID"`;
}

